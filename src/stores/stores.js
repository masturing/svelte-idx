import { writable } from "svelte/store";


function createCounter() {
    const {subscribe, set, update} =  writable(0)
    return {
        subscribe,
        increment: () => update(n => n + 1),
        decrement: () => update(n => n - 1),
        reset: () => set(0)
    }
}

export const counter = createCounter()

export const count = writable(0)